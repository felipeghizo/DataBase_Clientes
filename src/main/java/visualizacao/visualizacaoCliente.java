    private void editClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editClienteActionPerformed
        String clienteNome = JOptionPane.showInputDialog("Nome do cliente: ");
        String clienteNotaFiscal = JOptionPane.showInputDialog("Nota fiscal do cliente: ");
        int clienteid = cliente.getClienteid(clienteNome, clienteNotaFiscal);
        if(clienteid == -1){
            JOptionPane.showMessageDialog(null, "Dados não encontrados!");
        }else{
            // Definindo o look and feel do sistema operacional
            try {
                UIManager.setLookAndFeel(UIManager.getSystemLookAndFeelClassName());
            } catch (ClassNotFoundException | InstantiationException | IllegalAccessException | UnsupportedLookAndFeelException e) {
                e.printStackTrace();
            }

            // Opções de botões
            String[] options = {"Nome", "telefone", "E-mail", "Nota fiscal", "Endereço", "Cancelar"};

            // Mensagem da caixa de diálogo
            String message = "Qual dado você gostaria de alterar?";

            // Título da caixa de diálogo
            String title = "Editando dados de câmera";

            // Exibindo o JOptionPane com botões personalizados
            int option = JOptionPane.showOptionDialog(
                null,                                 // Componente pai
                message,                              // Mensagem
                title,                                // Título
                JOptionPane.YES_NO_CANCEL_OPTION,     // Tipo de opção
                JOptionPane.QUESTION_MESSAGE,         // Tipo de mensagem
                null,                                 // Ícone
                options,                              // Botões personalizados
                options[0]                            // Botão padrão
            );

            // Tratamento da opção selecionada
            switch (option) {
                case 0: // Nome
                String novoNome = JOptionPane.showInputDialog("Novo nome: ");
                cliente.setNome(novoNome);
                break;
                case 1: // Telefone
                String novoTelefone = JOptionPane.showInputDialog("Novo telefone: ");
                cliente.setTelefone(novoTelefone);
                break;
                case 2: // E-mail
                String novoEmail = JOptionPane.showInputDialog("Novo E-mail: ");
                cliente.setEmail(novoEmail);
                break;
                case 3: // Nota fiscal
                String novaNotaFiscal = JOptionPane.showInputDialog("Nova nota Fiscal: ");
                cliente.setNumeroCliente(novaNotaFiscal);
                break;
                case 4: // Endereço
                String novaEndereco = JOptionPane.showInputDialog("Novo Endereço: ");
                cliente.setEndereco(novaEndereco);
                break;
                case 5: // Cancelar
                JOptionPane.showMessageDialog(null, "Operação cancelada!");
                break;
                default:
                System.out.println("Nenhuma opção selecionada");
                break;
            }
        }
    }//GEN-LAST:event_editClienteActionPerformed

